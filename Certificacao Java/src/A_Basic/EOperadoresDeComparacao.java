package A_Basic;

public class EOperadoresDeComparacao {


    public static void main(String[] args) {
        boolean resultado = 10>10; //false
        System.out.println(resultado);

        resultado = 10<10; //false
        System.out.println(resultado);

        resultado = 10==10; //true
        System.out.println(resultado);

        resultado = 10>=10; //true
        System.out.println(resultado);

        resultado = 10!=10; //false
        System.out.println(resultado);
    }
}
