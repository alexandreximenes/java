    private void produtoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_produtoSalvarActionPerformed
        String codigo = produtoCodigo.getText();
        String descricao = produtoDescricao.getText();
        if(validarCampos(descricao)){
            int code = (produtoCodigo.getText().equals("")) ? 0 : Integer.parseInt(produtoCodigo.getText());
            Produto produto = new Produto(code, descricao);
            String msg = dao.salvar(produto);
            produto = dao.getProduto(descricao);
            this.tableModelProduto.addProduto(produto);
            JOptionPane.showMessageDialog(this, msg);
            limpar();
        }else{
            JOptionPane.showMessageDialog(this, "Favor preencher os campos!");
        }
        

    }//GEN-LAST:event_produtoSalvarActionPerformed

    private void produtoApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_produtoApagarActionPerformed
        String codigo = produtoCodigo.getText();
        String descricao = produtoDescricao.getText();
        if(validarCampos(codigo, descricao)){
            String msg = dao.excluir(produtoCodigo.getText());
            int linhaSelecionada = jTableProduto.getSelectedRow();
            this.tableModelProduto.removerProduto(linhaSelecionada);
            JOptionPane.showMessageDialog(this, msg);
            limpar();
        }else{
            JOptionPane.showMessageDialog(this, "Busque um cliente para deletar!");
        }

    }//GEN-LAST:event_produtoApagarActionPerformed

