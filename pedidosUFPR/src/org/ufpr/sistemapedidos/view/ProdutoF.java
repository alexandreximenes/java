    private void produtoBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_produtoBuscarKeyReleased
        if(produtoBuscar.getText().length()==0){
            produtoResposta.setText("");
        }
        Produto produto = dao.getProduto(produtoBuscar.getText());
        if(produto != null){
            produtoResposta.setText("");
            produtoCodigo.setText(String.valueOf(produto.getId()));
            produtoDescricao.setText(produto.getDescricao());
        }else{
            if(produtoBuscar.getText().length()>1){
                produtoCodigo.setText("");
                produtoDescricao.setText("");
                produtoResposta.setText("Nenhum produto encontrado");
            }
            
        }
    }//GEN-LAST:event_produtoBuscarKeyReleased

    private void jTableProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProdutoMouseClicked
        if(evt.getClickCount() == 1){
            int linhaSelecionada = jTableProduto.getSelectedRow();
            produtoCodigo.setText("" + jTableProduto.getValueAt(linhaSelecionada, 0));
            produtoDescricao.setText("" + jTableProduto.getValueAt(linhaSelecionada, 1));
        }
    }//GEN-LAST:event_jTableProdutoMouseClicked

